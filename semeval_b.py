def check_data_quality(df, subtask_name):
    """Identify potential data quality issues"""
    print(f"\n{'='*70}")
    print(f"DATA QUALITY CHECK - Subtask {subtask_name}")
    print(f"{'='*70}")

    issues = []

    # Check for empty codes
    if 'code' in df.columns:
        empty_codes = (df['code'].str.len() == 0).sum()
        if empty_codes > 0:
            issues.append(f"  ⚠ {empty_codes} empty code snippets")

    # Check for duplicates
    if 'code' in df.columns:
        duplicates = df['code'].duplicated().sum()
        if duplicates > 0:
            issues.append(f"  ⚠ {duplicates} duplicate code snippets")

    # Check for null values
    null_cols = df.isnull().sum()
    if null_cols.sum() > 0:
        for col, count in null_cols[null_cols > 0].items():
            issues.append(f"  ⚠ {count} null values in '{col}'")

    # Check for imbalanced labels
    if 'label' in df.columns:
        label_dist = df['label'].value_counts()
        max_count = label_dist.max()
        min_count = label_dist.min()
        imbalance_ratio = max_count / min_count if min_count > 0 else float('inf')

        if imbalance_ratio > 2:
            issues.append(f"  ⚠ Imbalanced labels (ratio: {imbalance_ratio:.2f})")

    # Check for garbled/corrupted code
    if 'code' in df.columns:
        # Simple heuristic: look for high proportion of special chars
        def has_suspicious_chars(code_str):
            total_chars = len(code_str)
            if total_chars == 0:
                return False
            special_chars = sum(1 for c in code_str if not c.isalnum() and c not in ' \n\t()[]{}.:,;"\'-')
            return special_chars / total_chars > 0.5

        corrupted = df['code'].apply(has_suspicious_chars).sum()
        if corrupted > 0:
            issues.append(f"  ℹ {corrupted} potentially corrupted/garbled code snippets")

    if not issues:
        print("✓ No major data quality issues detected")
    else:
        print("Issues found:")
        for issue in issues:
            print(issue)

    return issues


