def preprocess_code(code_str):
    """
    Basic code preprocessing
    - Strip whitespace
    - Remove comments (basic)
    - Normalize indentation
    """
    # Strip leading/trailing whitespace
    code = code_str.strip()

    # Remove single-line comments (simple heuristic)
    lines = code.split('\n')
    lines = [line.split('#')[0].rstrip() for line in lines]  # Python comments
    code = '\n'.join(lines)

    return code


def extract_code_features(code_str):
    """Extract basic features from code"""
    features = {
        'length': len(code_str),
        'lines': code_str.count('\n'),
        'indent_levels': len(set(len(line) - len(line.lstrip()) for line in code_str.split('\n'))),
        'avg_line_length': np.mean([len(line) for line in code_str.split('\n')]) if code_str.count('\n') > 0 else 0,
        'has_docstring': '"""' in code_str or "'''" in code_str,
        'has_type_hints': ':' in code_str,  # Simple heuristic
        'unique_tokens': len(set(code_str.split())),
    }
    return features


