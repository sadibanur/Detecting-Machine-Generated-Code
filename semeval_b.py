# Define the get_embeddings function
def get_embeddings(code_samples_list, tokenizer, model, device='cpu', batch_size=32):
    model.eval() # Set model to evaluation mode
    embeddings = []

    # Ensure code_samples_list is a list of strings
    if isinstance(code_samples_list, pd.Series):
        code_samples_list = code_samples_list.tolist()

    for i in range(0, len(code_samples_list), batch_size):
        batch_codes = code_samples_list[i:i + batch_size]
        # Tokenize batch
        inputs = tokenizer(batch_codes, return_tensors='pt', padding=True, truncation=True, max_length=512)
        inputs = {k: v.to(device) for k, v in inputs.items()}

        with torch.no_grad():
            outputs = model(**inputs)
        # Use the [CLS] token embedding for sentence representation
        batch_embeddings = outputs.last_hidden_state[:, 0, :].cpu().numpy()
        embeddings.append(batch_embeddings)
    return np.vstack(embeddings) if embeddings else np.array([])


